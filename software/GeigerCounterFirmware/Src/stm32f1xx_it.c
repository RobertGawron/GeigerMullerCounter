void uinttochar(char* a, unsigned int n)
{
  if (n == 0)
  {
    *a = '0';
    *(a+1) = '\0';
    return;
  }

  char aux[20];
  aux[19] = '\0';
  char* auxp = aux + 19;

  int c = 1;
  while (n != 0)
  {
    int mod = n % 10;
    *(--auxp) = mod | 0x30;
    n /=  10;
    c++;
  }

  memcpy(a, auxp, c);
}
